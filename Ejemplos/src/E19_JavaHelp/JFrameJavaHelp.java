    private void cargaAyuda() {
        HelpSet helpset;
        URL hsURL;
        try {    // dentro de un paquete src/ej17JavaHelp/help/help_set.hs
            File fichero = new File("src/E19_JavaHelp/help/help_set.hs");
            hsURL = fichero.toURI().toURL();
            helpset = new HelpSet(getClass().getClassLoader(), hsURL);
            HelpBroker hb = helpset.createHelpBroker();
            hb.enableHelpOnButton(jMenuItemAyuda, "aplicacion", helpset);
            System.out.println(hsURL.getFile());
        } catch (HelpSetException ex) {
            System.err.println("Error HelpSetException");
        } catch (MalformedURLException me) {
            System.err.println("Fallo al crear URL");
        }
    }
    
